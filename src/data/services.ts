export function getServiceById(id: string): Service | undefined {
  return services.find((s) => s.id === id)
}

export function getServicesByCategory(slug: string): Service[] {
  return services.filter((s) => s.categorySlug === slug)
}

export function getFreelancerForService(service: Service) {
  return freelancers.find((f) => f.id === service.freelancerId)
}
